def apply_tecan_unit_conversion(field_name, field_value):
    """Apply Tecan-specific unit conversions based on field type"""
    try:
        # Convert field name to lowercase for easier matching
        field_name_lower = field_name.lower()
        
        # WAVELENGTH CONVERSION: multiply by 10 (nm -> tenths of nm)
        if 'wavelength' in field_name_lower:
            try:
                numeric_value = float(field_value)
                converted_value = int(numeric_value * 10)
                return converted_value
            except (ValueError, TypeError):
                return field_value
        
        # DEFAULT: No conversion needed
        return field_value
            
    except Exception as e:
        return field_value  # Return original value if conversion fails

