def load_parameter_combinations():
    """Load parameter combinations from JSON file generated by optimization campaign wizard"""
    param_file = Path("parameter_combinations.json")
    
    if not param_file.exists():
        raise FileNotFoundError(f"Parameter combinations file not found: {param_file}")
    
    print(f"Loading parameter combinations from: {param_file}")
    with open(param_file, 'r') as f:
        parameter_combinations = json.load(f)
    
    # Validate structure
    if not isinstance(parameter_combinations, list):
        raise ValueError("Parameter combinations must be a list")
    
    for i, combo in enumerate(parameter_combinations):
        required_keys = ['combination_id', 'parameters']
        for key in required_keys:
            if key not in combo:
                raise ValueError(f"Parameter combination {i} missing required key: {key}")
    
    print(f"Successfully loaded {len(parameter_combinations)} parameter combinations")
    return parameter_combinations

def load_well_mapping():
    """Load well mapping from JSON file generated by optimization campaign wizard"""
    well_file = Path("well_mapping.json")
    
    if not well_file.exists():
        raise FileNotFoundError(f"Well mapping file not found: {well_file}")
    
    print(f"Loading well mapping from: {well_file}")
    with open(well_file, 'r') as f:
        well_mapping = json.load(f)
    
    # Validate structure
    if not isinstance(well_mapping, list):
        raise ValueError("Well mapping must be a list")
    
    for i, mapping in enumerate(well_mapping):
        required_keys = ['combination_id', 'wells', 'replicate_count']
        for key in required_keys:
            if key not in mapping:
                raise ValueError(f"Well mapping {i} missing required key: {key}")
    
    print(f"Successfully loaded {len(well_mapping)} well mappings")
    return well_mapping

def process_autonomous_opentrons_file(filename, parameter_combinations, well_mapping):
    """Process Opentrons file for autonomous optimization"""
    protocols_folder = Path("protocols")
    file_path = protocols_folder / filename
    
    if not file_path.exists():
        return {}
    
    print(f"Processing Opentrons file for autonomous optimization: {filename}")
    
    with open(file_path, 'r', encoding='utf-8') as f:
        content = f.read()
    
    replacements_made = {}
    
    # Replace [[PARAMETER_COMBINATIONS]] placeholder
    if "[[PARAMETER_COMBINATIONS]]" in content:
        combinations_json = json.dumps(parameter_combinations, indent=4)
        content = content.replace("[[PARAMETER_COMBINATIONS]]", combinations_json)
        replacements_made["[[PARAMETER_COMBINATIONS]]"] = f"{len(parameter_combinations)} combinations"
        print(f"  Replaced [[PARAMETER_COMBINATIONS]] with {len(parameter_combinations)} combinations")
    
    # Replace [[WELL_MAPPING]] placeholder
    if "[[WELL_MAPPING]]" in content:
        well_mapping_json = json.dumps(well_mapping, indent=4)
        content = content.replace("[[WELL_MAPPING]]", well_mapping_json)
        replacements_made["[[WELL_MAPPING]]"] = f"{len(well_mapping)} mappings"
        print(f"  Replaced [[WELL_MAPPING]] with {len(well_mapping)} well mappings")
    
    # Write back to file
    with open(file_path, 'w', encoding='utf-8') as f:
        f.write(content)
    
    return replacements_made

def process_autonomous_tecan_file(filename, parameter_combinations, well_mapping):
    """Process Tecan file for autonomous optimization"""
    protocols_folder = Path("protocols")
    file_path = protocols_folder / filename
    
    if not file_path.exists():
        return {}
    
    print(f"Processing Tecan file for autonomous optimization: {filename}")
    
    with open(file_path, 'r', encoding='utf-8') as f:
        content = f.read()
    
    replacements_made = {}
    
    # For Tecan XML files, we typically need to replace measurement parameters
    # but maintain the parameter combinations structure in comments or metadata
    
    # Add parameter combinations as XML comment for reference
    if parameter_combinations:
        param_comment = f"<!-- Parameter Combinations: {json.dumps(parameter_combinations, indent=2)} -->"
        content = param_comment + "\n" + content
        replacements_made["parameter_combinations_comment"] = f"{len(parameter_combinations)} combinations added as comment"
    
    # Replace any Tecan-specific placeholders using loaded mappings
    tecan_mappings = load_tecan_mappings()
    for placeholder, field_name in tecan_mappings.items():
        if placeholder in content:
            # For autonomous mode, we might need to use a default value or the first combination's value
            if parameter_combinations:
                first_combo_params = parameter_combinations[0]["parameters"]
                if field_name in first_combo_params:
                    value = first_combo_params[field_name]
                    content = content.replace(placeholder, str(value))
                    replacements_made[placeholder] = value
                    print(f"  Replaced {placeholder} -> {value} (from first combination)")
    
    # Write back to file
    with open(file_path, 'w', encoding='utf-8') as f:
        f.write(content)
    
    return replacements_made

def process_evaluation_script(filename, parameter_combinations, well_mapping, experiment_id):
    """Process evaluation script to include parameter combinations and well mapping"""
    analysis_folder = Path("../analysis")  # Evaluation scripts are typically in analysis folder
    file_path = analysis_folder / filename
    
    if not file_path.exists():
        print(f"  Warning: Evaluation script not found: {file_path}")
        return {}
    
    print(f"Processing evaluation script: {filename}")
    
    with open(file_path, 'r', encoding='utf-8') as f:
        content = f.read()
    
    replacements_made = {}
    
    # Create optimization data structure for the evaluation script
    optimization_data = {
        "experiment_id": experiment_id,
        "parameter_combinations": parameter_combinations,
        "well_mapping": well_mapping,
        "generated_timestamp": datetime.utcnow().isoformat()
    }
    
    # Save optimization data to a JSON file that the evaluation script can load
    data_file_path = Path("../data") / f"optimization_data_{experiment_id}.json"
    data_file_path.parent.mkdir(exist_ok=True)
    
    with open(data_file_path, 'w') as f:
        json.dump(optimization_data, f, indent=2)
    
    replacements_made["optimization_data_file"] = str(data_file_path)
    
    # If the evaluation script has placeholders, replace them
    if "[[PARAMETER_COMBINATIONS]]" in content:
        combinations_json = json.dumps(parameter_combinations, indent=4)
        content = content.replace("[[PARAMETER_COMBINATIONS]]", combinations_json)
        replacements_made["[[PARAMETER_COMBINATIONS]]"] = f"{len(parameter_combinations)} combinations"
    
    if "[[WELL_MAPPING]]" in content:
        well_mapping_json = json.dumps(well_mapping, indent=4)
        content = content.replace("[[WELL_MAPPING]]", well_mapping_json)
        replacements_made["[[WELL_MAPPING]]"] = f"{len(well_mapping)} mappings"
    
    # Replace experiment ID placeholder if present
    if "[[EXPERIMENT_ID]]" in content:
        content = content.replace("[[EXPERIMENT_ID]]", str(experiment_id))
        replacements_made["[[EXPERIMENT_ID]]"] = experiment_id
    
    # Write back to file
    with open(file_path, 'w', encoding='utf-8') as f:
        f.write(content)
    
    print(f"  Created optimization data file: {data_file_path}")
    
    return replacements_made

