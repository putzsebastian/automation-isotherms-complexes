def process_automated_file(filename, experiment_data, mappings, file_type="opentrons"):
    """Process file using automated mode (individual placeholder replacement)"""
    if file_type == "opentrons":
        protocols_folder = Path("protocols")
    else:
        protocols_folder = Path("protocols")
    
    file_path = protocols_folder / filename
    
    if not file_path.exists():
        return {}
    
    print(f"Processing {file_type} file (automated mode): {filename}")
    
    with open(file_path, 'r', encoding='utf-8') as f:
        content = f.read()
    
    replacements = replace_placeholders(content, experiment_data, mappings)
    
    with open(file_path, 'w', encoding='utf-8') as f:
        f.write(replacements["content"])
    
    return replacements["replacements"]

def replace_placeholders(content, experiment_data, mappings):
    """Replace individual placeholders in content with experiment values (automated mode)"""
    metadata = experiment_data.get('metadata_decoded', {})
    extra_fields = metadata.get('extra_fields', {})
    
    replaced_content = content
    replacements_made = {}
    
    for placeholder, field_name in mappings.items():
        if field_name in extra_fields:
            field_value = extra_fields[field_name].get('value', '')
            if field_value:
                # Apply Tecan wavelength conversion (×10 for nm -> tenths of nm)
                converted_value = apply_tecan_unit_conversion(field_name, field_value)
                replaced_content = replaced_content.replace(placeholder, str(converted_value))
                replacements_made[placeholder] = converted_value
                if converted_value != field_value:
                    print(f"  Replaced {placeholder} -> {converted_value} (converted from {field_value})")
                else:
                    print(f"  Replaced {placeholder} -> {field_value}")
        else:
            print(f"  Warning: Field '{field_name}' not found for {placeholder}")
    
    return {"content": replaced_content, "replacements": replacements_made}

def generate_protocols_for_experiment(experiment_id):
    """Main function: Process files based on autonomous vs automated mode"""
    print(f"Processing experiment {experiment_id}...")
    
    # Load experiment data
    with open(f"experiment_{experiment_id}.json", 'r') as f:
        experiment_data = json.load(f)
    
    # Determine processing mode by checking for autonomous data files
    is_autonomous = determine_process_mode()
    
    all_replacements = {}
    modified_files = []
    
    if is_autonomous:
        print("🎯 AUTONOMOUS MODE: Loading parameter combinations and well mapping from files")
        
        try:
            # Load parameter combinations and well mapping from JSON files
            parameter_combinations = load_parameter_combinations()
            well_mapping = load_well_mapping()
            
            print(f"Loaded {len(parameter_combinations)} parameter combinations")
            print(f"Loaded {len(well_mapping)} well mappings")
            
            # Validate consistency between parameter combinations and well mapping
            param_ids = {combo['combination_id'] for combo in parameter_combinations}
            mapping_ids = {mapping['combination_id'] for mapping in well_mapping if mapping['combination_id'] >= 0}  # Exclude controls
            
            missing_mappings = param_ids - mapping_ids
            if missing_mappings:
                print(f"⚠️  Warning: Parameter combinations without well mappings: {missing_mappings}")
            
            extra_mappings = mapping_ids - param_ids
            if extra_mappings:
                print(f"⚠️  Warning: Well mappings without parameter combinations: {extra_mappings}")
            
        except (FileNotFoundError, ValueError) as e:
            print(f"❌ Error loading autonomous data files: {e}")
            print("Falling back to automated mode...")
            is_autonomous = False
    
    if is_autonomous:
        # Process Opentrons files (autonomous mode)
        for filename in OPENTRONS_FILES:
            replacements = process_autonomous_opentrons_file(filename, parameter_combinations, well_mapping)
            all_replacements.update(replacements)
            if replacements:
                modified_files.append(f"protocols/{filename}")
        
        # Process Tecan files (autonomous mode)
        for filename in TECAN_FILES:
            replacements = process_autonomous_tecan_file(filename, parameter_combinations, well_mapping)
            all_replacements.update(replacements)
            if replacements:
                modified_files.append(f"protocols/{filename}")
        
        # Process evaluation scripts
        for filename in EVALUATION_SCRIPTS:
            replacements = process_evaluation_script(filename, parameter_combinations, well_mapping, experiment_id)
            all_replacements.update(replacements)
            if replacements:
                modified_files.append(f"analysis/{filename}")
        
        # Save autonomous experiment data for reference
        autonomous_data = {
            "experiment_id": experiment_id,
            "mode": "autonomous",
            "parameter_combinations_file": "parameter_combinations.json",
            "well_mapping_file": "well_mapping.json",
            "parameter_combinations_count": len(parameter_combinations),
            "well_mappings_count": len(well_mapping),
            "timestamp": datetime.utcnow().isoformat()
        }
        
        with open(f"autonomous_experiment_{experiment_id}.json", 'w') as f:
            json.dump(autonomous_data, f, indent=2)
        
        print(f"✅ Autonomous experiment log saved to: autonomous_experiment_{experiment_id}.json")
        
    else:
        print("🔧 AUTOMATED MODE: Individual placeholder replacement")
        
        # Process Opentrons files (automated mode)
        for filename in OPENTRONS_FILES:
            replacements = process_automated_file(filename, experiment_data, OPENTRONS_MAPPINGS, "opentrons")
            all_replacements.update(replacements)
            if replacements:
                modified_files.append(f"protocols/{filename}")
        
        # Process Tecan files (automated mode)
        for filename in TECAN_FILES:
            replacements = process_automated_file(filename, experiment_data, TECAN_MAPPINGS, "tecan")
            all_replacements.update(replacements)
            if replacements:
                modified_files.append(f"protocols/{filename}")
    
    # Save log
    log_data = {
        "experiment_id": experiment_id,
        "mode": "autonomous" if is_autonomous else "automated",
        "timestamp": datetime.utcnow().isoformat(),
        "replacements_made": all_replacements,
        "modified_files": modified_files,
        "template_id": TEMPLATE_ID,
        "process_id": PROCESS_ID,
        "autonomous_data": {
            "parameter_combinations_count": len(parameter_combinations) if is_autonomous else 0,
            "well_mappings_count": len(well_mapping) if is_autonomous else 0,
            "data_files_found": is_autonomous
        }
    }
    
    with open("protocol_generation_log.json", 'w') as f:
        json.dump(log_data, f, indent=2)
    
    mode_str = "AUTONOMOUS" if is_autonomous else "AUTOMATED"
    print(f"✅ {mode_str} processing complete! {len(all_replacements)} replacements in {len(modified_files)} files")
    
    return {
        "success": True,
        "mode": "autonomous" if is_autonomous else "automated",
        "replacements": all_replacements,
        "modified_files": modified_files,
        "opentrons_files_processed": len([f for f in OPENTRONS_FILES if f"protocols/{f}" in modified_files]),
        "tecan_files_processed": len([f for f in TECAN_FILES if f"protocols/{f}" in modified_files]),
        "evaluation_scripts_processed": len([f for f in EVALUATION_SCRIPTS if f"analysis/{f}" in modified_files]) if is_autonomous else 0,
        "data_files_loaded": is_autonomous
    }

